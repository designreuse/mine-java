package cup;

public class Entity {
	
	public String name;
	public int count;
	public double score;
	
	public Entity(String name) {
		this.name = name;
	}
	
	public Entity(String name, int count, double score) {
		this.name = name;
		this.count = count;
		this.score = score;
	}
	
	
}
